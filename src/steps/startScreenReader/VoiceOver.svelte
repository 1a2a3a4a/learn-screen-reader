<p>Enable Voiceover on Mac by going to System Preferences then Accessibility then Voiceover and click the checkbox to enable voiceover</p>
<p>You can start (or stop) VoiceOver by pressing <kbd>Command</kbd> + <kbd>F5</kbd>.</p>

<p>The VoiceOver Activation keys (called <strong>VO</strong> keys) are <kbd>control</kbd> + <kbd>option</kbd> and will be referenced simply as <kbd>VO</kbd>. For example, <kbd>VO</kbd> + <kbd>H</kbd> (or <kbd>control</kbd> + <kbd>option</kbd> + <kbd>H</kbd>) will open the VoiceOver Help menu.</p>

<p>Voiceover works best with Safari and also make sure that if you are using safari then enable accessibility by going to Advanced then Accessibility and presse Tab to highlight each item on webpage.</p>